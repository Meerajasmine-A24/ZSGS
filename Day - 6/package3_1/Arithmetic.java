package mypack;

public class Arithmetic{
	
	public int add(int a,int b){
		return a+b;
	}
	
	public int sub(int a,int b){
		return a-b;
	}
	
	public int multiply(int a,int b){
		return a*b;
	}
	
	public int divison(int a,int b){
		return a/b;
	}
	
	public int modulo(int a,int b){
		return a%b;
	}
}
